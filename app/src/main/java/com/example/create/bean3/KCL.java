public class KCL {
    private  String  ylm,xh,kc;

    public KCL(String ylm, String xh, String kc) {
        this.ylm = ylm;
        this.xh = xh;
        this.kc = kc;
    }

    public String getYlm() {
        return ylm;
    }

    public void setYlm(String ylm) {
        this.ylm = ylm;
    }

    public String getXh() {
        return xh;
    }

    public void setXh(String xh) {
        this.xh = xh;
    }

    public String getKc() {
        return kc;
    }

    public void setKc(String kc) {
        this.kc = kc;
    }
}
